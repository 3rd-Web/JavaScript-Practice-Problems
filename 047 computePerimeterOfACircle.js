// Write a function called computePerimeterOfACircle. Given the radius of a circle computePerimeterOfACircle returns its perimeter.

function computePerimeterOfACircle(radius) {
  // your code here
  return 2 * Math.PI * radius;
}

let output = computePerimeterOfACircle(4);
console.log(output); // --> 25.132741228718345
